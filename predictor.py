    def overlay_saliency(self, image, saliency, panoptic_seg, segments_info, n=2):
        panoptic_seg = panoptic_seg.to(self.cpu_device)

        scores = []
        unique_entries = np.unique(panoptic_seg)

        def find_segment_info(segments_info, entry):
            for info in segments_info:
                if info['id'] == entry:
                    return info
            return None

        for entry in unique_entries:
            if entry == 0:
                continue
            
            segment = find_segment_info(segments_info, entry)
            if segment and segment['isthing']:
                score = np.sum(saliency[panoptic_seg == entry])
                scores.append([score, entry])

        if len(scores) == 0:
            return image, image * 0
        
        scores = np.array(scores)
        best_scores = scores[np.argsort(scores[:, 0])][-n:]

        best_entries = [score[1] for score in best_scores]
        best_mask = np.isin(panoptic_seg, best_entries)
        panoptic_seg[~best_mask] = 0
        segments_info = [info for info in segments_info if info['id'] in best_entries]

